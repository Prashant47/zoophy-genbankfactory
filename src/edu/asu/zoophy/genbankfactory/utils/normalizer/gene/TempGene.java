package edu.asu.zoophy.genbankfactory.utils.normalizer.gene;

public class TempGene {
	
	private long id;
	private String name;
	private int taxon;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTaxon() {
		return taxon;
	}
	public void setTaxon(int taxon) {
		this.taxon = taxon;
	}
}
